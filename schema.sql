DROP TABLE IF EXISTS `users`;
DROP TABLE IF EXISTS `maps`;
DROP TABLE IF EXISTS `wh_systems`;
DROP TABLE IF EXISTS `wh_types`;
DROP TABLE IF EXISTS `log`;

CREATE TABLE `users` (
	`id` int unsigned NOT NULL AUTO_INCREMENT,
	`username` varchar(64) NOT NULL UNIQUE,
	`password` char(64) NOT NULL,
	`salt` char(32) NOT NULL,
	`admin` boolean NOT NULL,
	PRIMARY KEY (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_bin;

CREATE TABLE `maps` (
	`json` text NOT NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_bin;

CREATE TABLE `logs` (
	`id` int unsigned NOT NULL AUTO_INCREMENT,
	`time` datetime NOT NULL,
	`user_id` int unsigned NOT NULL,
	`action_id` tinyint NOT NULL,
	`log_message` varchar(65) NOT NULL,
	PRIMARY KEY (`id`),
	INDEX `i_log_time` (`time`),
	FOREIGN KEY (user_id) REFERENCES users(id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_bin;

CREATE TABLE `wh_types` (
	`id` tinyint unsigned NOT NULL,
	`name` char(4) NOT NULL,
	`src` varchar(11) NOT NULL,
	`dest` varchar(8) NOT NULL,
	`lifetime` tinyint NOT NULL,
	`jump_mass` tinyint NOT NULL,
	`max_mass` smallint NOT NULL,
	PRIMARY KEY (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_bin;

CREATE TABLE `wh_systems` (
	`id` int unsigned NOT NULL,
	`name` char(7) NOT NULL UNIQUE,
	`class` tinyint NOT NULL,
	`effect` varchar(20) NOT NULL,
	`static1` tinyint unsigned NOT NULL,
	`static2` tinyint unsigned NULL,
	PRIMARY KEY (`id`),
	CONSTRAINT `wh_static1` FOREIGN KEY (`static1`) REFERENCES `wh_types` (`id`),
	CONSTRAINT `wh_static2` FOREIGN KEY (`static2`) REFERENCES `wh_types` (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_bin;

